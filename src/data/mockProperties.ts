export const getPropertiesByCategory = (category: string): Property[] => {
  return mockProperties.filter(p => p.category === category);
};

export const getFeaturedProperties = (): Property[] => {
  return mockProperties.filter(p => p.featured);
};

export const searchProperties = (query: string): Property[] => {
  const lowercaseQuery = query.toLowerCase();
  return mockProperties.filter(p =>
    p.title.toLowerCase().includes(lowercaseQuery) ||
    p.description.toLowerCase().includes(lowercaseQuery) ||
    p.location.town.toLowerCase().includes(lowercaseQuery) ||
    p.location.country.toLowerCase().includes(lowercaseQuery)
  );
};
