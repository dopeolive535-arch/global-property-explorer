export const getFeaturedProperties = (): Property[] => {
  return mockProperties.filter(p => p.featured);
};

export const searchProperties = (query: string): Property[] => {
  const lowercaseQuery = query.toLowerCase();
  return mockProperties.filter(p =>
    p.title.toLowerCase().includes(lowercaseQuery) ||
    p.description.toLowerCase().includes(lowercaseQuery) ||
    p.location.town.toLowerCase().includes(lowercaseQuery) ||
    p.location.country.toLowerCase().includes(lowercaseQuery) ||
    p.category.toLowerCase().includes(lowercaseQuery) ||
    p.location.neighborhood.toLowerCase().includes(lowercaseQuery) ||
    p.location.district.toLowerCase().includes(lowercaseQuery) ||
    p.location.region.toLowerCase().includes(lowercaseQuery)
  );
};

export const fuzzySearchProperties = (query: string): Property[] => {
  const lowercaseQuery = query.toLowerCase().trim();

  if (!lowercaseQuery) return mockProperties;

  const searchTerms = lowercaseQuery.split(' ').filter(term => term.length > 0);

  return mockProperties.filter(property => {
    const searchableText = `
      ${property.title}
      ${property.description}
      ${property.category}
      ${property.location.country}
      ${property.location.region}
      ${property.location.district}
      ${property.location.town}
      ${property.location.neighborhood}
      ${property.amenities.join(' ')}
      ${property.furnished}
      ${property.listingType}
    `.toLowerCase();

    return searchTerms.some(term => searchableText.includes(term)) ||
           searchTerms.every(term => {
             const fuzzyMatch = term.split('').some((char, i) => {
               return searchableText.includes(term.substring(0, i + 1));
             });
             return fuzzyMatch;
           });
  }).sort((a, b) => {
    const aScore = searchTerms.filter(term =>
      a.title.toLowerCase().includes(term) ||
      a.location.town.toLowerCase().includes(term)
    ).length;
    const bScore = searchTerms.filter(term =>
      b.title.toLowerCase().includes(term) ||
      b.location.town.toLowerCase().includes(term)
    ).length;
    return bScore - aScore;
  });
};
